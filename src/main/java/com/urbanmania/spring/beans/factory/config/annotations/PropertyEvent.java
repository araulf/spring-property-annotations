package com.urbanmania.spring.beans.factory.config.annotations;

import java.util.EventObject;

/**
 * @author Ricardo Gladwell <ricardo.gladwell@gmail.com>
 */
public class PropertyEvent extends EventObject {

	private static final long serialVersionUID = 6053999887374367052L;

	String key;
	String value;

	public PropertyEvent(Object source, String key, String value) {
		super(source);
		this.key = key;
		this.value = value;
	}

	public String getKey() {
		return key;
	}

	public String getValue() {
		return value;
	}

	@Override
	public String toString() {
		return getClass().getName()+"@"+super.hashCode()+"[key="+key+",value="+value+"]";
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((key == null) ? 0 : key.hashCode());
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		PropertyEvent other = (PropertyEvent) obj;
		if (key == null) {
			if (other.key != null)
				return false;
		} else if (!key.equals(other.key))
			return false;
		if (value == null) {
			if (other.value != null)
				return false;
		} else if (!value.equals(other.value))
			return false;
		return true;
	}

}
